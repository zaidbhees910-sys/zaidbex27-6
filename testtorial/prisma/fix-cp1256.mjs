// prisma/fix-cp1256.mjs
// Fixes text in the database that was stored as UTF-8 bytes decoded via Windows-1256 (CP1256).
// The UTF-8 bytes of Arabic characters were misinterpreted as CP1256 codepoints and stored.
// Fix: map each stored codepoint back to its original byte, then decode as UTF-8.
// Run: node prisma/fix-cp1256.mjs

import { PrismaClient } from '@prisma/client';
const prisma = new PrismaClient();

