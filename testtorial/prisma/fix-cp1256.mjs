// Reverse: Unicode codepoint → CP1256 byte
const UNICODE_TO_CP1256 = new Map();
for (let b = 0x80; b <= 0xFF; b++) UNICODE_TO_CP1256.set(CP1256[b], b);
for (let b = 0x00; b < 0x80; b++) UNICODE_TO_CP1256.set(b, b);

/**
 * Reverses CP1256-misread UTF-8:
 * Each character's codepoint is converted back to its CP1256 byte,
 * then the resulting byte array is decoded as UTF-8.
 */
function fixCP1256(str) {
  if (!str) return str;
  const bytes = [];
  let needsFix = false;

  for (const char of str) {
    const cp = char.codePointAt(0);
    if (cp < 0x80) {
      bytes.push(cp);
    } else {
      const byte = UNICODE_TO_CP1256.get(cp);
      if (byte !== undefined) {
        bytes.push(byte);
        // If we encounter a CP1256-specific codepoint (non-Latin-1), it needs fixing
        if (cp > 0xFF || (cp >= 0x0600 && cp <= 0x06FF)) needsFix = true;
      } else {
        // Codepoint not in CP1256 range — encode as UTF-8 bytes directly
        const encoded = Buffer.from(char, 'utf-8');
        for (const b of encoded) bytes.push(b);
      }
    }
  }

  if (!needsFix) return str;

  try {
    const decoded = Buffer.from(bytes).toString('utf-8');
    // Verify result contains Arabic (sanity check)
    if (/[؀-ۿ؀-ۿ]/.test(decoded) || decoded !== str) return decoded;
  } catch {
    // fall through
  }
  return str;
}

async function main() {
  let total = 0;

  // Fix Variant fields
  const variants = await prisma.variant.findMany({
    select: { id: true, name: true, brand: true, specifications: true },
  });
  for (const v of variants) {
    const n = fixCP1256(v.name);
    const b = fixCP1256(v.brand);
    const s = fixCP1256(v.specifications);
    if (n === v.name && b === v.brand && s === v.specifications) continue;
    await prisma.variant.update({ where: { id: v.id }, data: { name: n, brand: b, specifications: s } });
    console.log(`[V${v.id}] "${v.name}" → "${n}"`);
    if (s !== v.specifications) console.log(`       specs fixed (${s.slice(0,50).replace(/\n/g,' ')}...)`);
    total++;
  }

  // Fix Product fields
  const products = await prisma.product.findMany({
    select: { id: true, name: true, category: true, description: true },
  });
  for (const p of products) {
    const n = fixCP1256(p.name);
    const c = fixCP1256(p.category);
    const d = fixCP1256(p.description);
    if (n === p.name && c === p.category && d === p.description) continue;
    await prisma.product.update({ where: { id: p.id }, data: { name: n, category: c, description: d } });
    console.log(`[P${p.id}] "${p.name}" → "${n}"`);
    total++;
  }

  console.log(`\nDone: ${total} records fixed.`);
  await prisma.$disconnect();
}

main().catch(e => { console.error(e); process.exit(1); });
