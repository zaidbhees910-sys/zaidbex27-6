async function main() {
  let count = 0;
  for (const [idStr, specifications] of Object.entries(specs)) {
    const id = parseInt(idStr);
    await prisma.variant.update({ where: { id }, data: { specifications } });
    const preview = specifications.split('\n')[0];
    console.log(`[V${id}] ✓ "${preview}"`);
    count++;
  }
  console.log(`\nDone: ${count} variants restored.`);
  await prisma.$disconnect();
}

main().catch(e => { console.error(e); process.exit(1); });
